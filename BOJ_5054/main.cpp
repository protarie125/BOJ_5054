using pll = pair<ll, ll>;
using vll = vector<pll>;
using vs = vector<string>;

int main() {
	ios::sync_with_stdio(false);
	std::cin.tie(nullptr);

	int t;
	cin >> t;
	while (0 < (t--)) {
		int n;
		cin >> n;

		auto xs = vi(n);

		auto minx = 100;
		auto maxx = 0;
		for (auto&& x : xs) {
			cin >> x;

			if (x < minx) {
				minx = x;
			}

			if (maxx < x) {
				maxx = x;
			}
		}

		cout << (maxx - minx) * 2 << '\n';
	}

	return 0;
}